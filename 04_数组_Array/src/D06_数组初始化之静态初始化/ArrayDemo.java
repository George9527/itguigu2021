package D06_数组初始化之静态初始化;

public class ArrayDemo {
    public static void main(String[] args) {
        //定义数组
        int[] arr = {1,2,3};

        //输出数组名(地址值)
        System.out.println(arr);

        //输出数组中元素
        System.out.println(arr[0]);
        System.out.println(arr[1]);
        System.out.println(arr[2]);
    }
}
